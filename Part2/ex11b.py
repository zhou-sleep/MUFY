# - A number
# - An operator ("+", "-", "*", or "/")
# - Another number
# The function should return the result of the calculation
def calculate(a, operation, b):
    if operation == "+":
        return a + b
    elif operation == "-":
        return a - b
    elif operation == "*":
        return a * b
    elif operation == "/":
        return a / b
    return "invalid operation"

